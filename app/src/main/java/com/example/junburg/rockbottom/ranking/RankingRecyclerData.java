public class RankingRecyclerData {

    private String rankingNickName;
    private String rankingPoint;
    private String rankingMessage;
    private String rankingTeam;
    private String rankingGithub;
    private String rankingNumber;

    public String getRankingNickName() {
        return rankingNickName;
    }

    public void setRankingNickName(String rankingNickName) {
        this.rankingNickName = rankingNickName;
    }

    public String getRankingPoint() {
        return rankingPoint;
    }

    public void setRankingPoint(String rankingPoint) {
        this.rankingPoint = rankingPoint;
    }

    public String getRankingMessage() {
        return rankingMessage;
    }

    public void setRankingMessage(String rankingMessage) {
        this.rankingMessage = rankingMessage;
    }

    public String getRankingTeam() {
        return rankingTeam;
    }

    public void setRankingTeam(String rankingTeam) {
        this.rankingTeam = rankingTeam;
    }

    public String getRankingGithub() {
        return rankingGithub;
    }

    public void setRankingGithub(String rankingGithub) {
        this.rankingGithub = rankingGithub;
    }

    public String getRankingNumber() {
        return rankingNumber;
    }

    public void setRankingNumber(String rankingNumber) {
        this.rankingNumber = rankingNumber;
    }
}
